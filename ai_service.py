    def _clean_response(self, response: str) -> str:
        """–û—á–∏—â–∞–µ—Ç –æ—Ç–≤–µ—Ç –æ—Ç –Ω–µ–Ω—É–∂–Ω—ã—Ö —Ñ—Ä–∞–∑ –∏ —Ñ–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç"""
        
        if not response:
            return ""
        
        # –£–±–∏—Ä–∞–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–µ —à–∞–±–ª–æ–Ω–Ω—ã–µ —Ñ—Ä–∞–∑—ã
        phrases_to_remove = [
            "–ö–æ–Ω–µ—á–Ω–æ, —è –ø–æ–º–æ–≥—É –≤–∞–º —Å —ç—Ç–∏–º.",
            "–Ø –ø–æ–Ω–∏–º–∞—é, —á—Ç–æ –≤—ã —á—É–≤—Å—Ç–≤—É–µ—Ç–µ.",
            "–°–ø–∞—Å–∏–±–æ, —á—Ç–æ –ø–æ–¥–µ–ª–∏–ª–∏—Å—å —Å–æ –º–Ω–æ–π.",
            "–ö–∞–∫ –∏—Å–∫—É—Å—Å—Ç–≤–µ–Ω–Ω—ã–π –∏–Ω—Ç–µ–ª–ª–µ–∫—Ç, —è –º–æ–≥—É —Å–∫–∞–∑–∞—Ç—å, —á—Ç–æ",
            "–ù–∞ –æ—Å–Ω–æ–≤–µ –≤–∞—à–µ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è,",
            "–£–≤–∞–∂–∞–µ–º—ã–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å,",
            "–î–æ—Ä–æ–≥–æ–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å,"
        ]
        
        cleaned = response.strip()
        for phrase in phrases_to_remove:
            if cleaned.startswith(phrase):
                cleaned = cleaned[len(phrase):].strip()
        
        # –£–±–∏—Ä–∞–µ–º –¥–≤–æ–π–Ω—ã–µ –ø—Ä–æ–±–µ–ª—ã –∏ –ª–∏—à–Ω–∏–µ –ø–µ—Ä–µ–Ω–æ—Å—ã
        cleaned = ' '.join(cleaned.split())
        
        # –ï—Å–ª–∏ –æ—Ç–≤–µ—Ç —Å–ª–∏—à–∫–æ–º –∫–æ—Ä–æ—Ç–∫–∏–π, –¥–æ–±–∞–≤–ª—è–µ–º —ç–º–æ–¥–∑–∏
